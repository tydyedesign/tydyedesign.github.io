 $(document).ready(function() {
    $('.rollme').mouseenter(function() {
        $('.rollme').fadeTo('fast', 0.5);
        $('.captionme').fadeTo('fast', 1);
    });
    
    $('.rollme').mouseleave(function() {
        $('.rollme').fadeTo('fast', 1);
        $('.captionme').fadeTo('fast', 0);
});
                
        
});
            
